public class AppOperators {
    public static void main(String[] args) {
        System.out.println(2 + 5);
        System.out.println(2 - 5);
        System.out.println(2 * 5);
        System.out.println(2 / 5);
        System.out.println(5 % 2);
        System.out.println(2 > 5);
        System.out.println(2 < 5);
        System.out.println(2 > 5 && 10 > 3);
        System.out.println(2 > 5 || 10 > 3);
    }
}
